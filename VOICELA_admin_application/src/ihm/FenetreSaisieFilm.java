    private void ButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAjouterActionPerformed
        // TODO add your handling code here:
        try {
            //nom
            if (jTextField1.getText().isEmpty()) {
                throw new Exception("champ numéro visa vide");
            }
            if (jTextField2.getText().isEmpty()) {
                throw new Exception("champ titre film vide");
            }
            if (jTextField3.getText().isEmpty()) {
                throw new Exception("champ année film vide");
            }
            
            film.setNumVisa(Integer.parseInt(jTextField1.getText()));
            film.setTitreFilm(jTextField2.getText());
            film.setAnneeFilm(Integer.parseInt(jTextField3.getText()));
            String genre = jComboBox1.getSelectedItem().toString();
            int numGenre = leDaoFilm.getNumGenre(genre);
            film.setNumGenre(numGenre);

            etatSortie = true;
            this.dispose();
            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(FenetreApplication.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_ButtonAjouterActionPerformed

