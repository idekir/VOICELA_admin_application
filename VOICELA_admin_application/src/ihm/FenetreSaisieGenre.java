    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int ligne = jTable1.getSelectedRow();
            Genre genre = leModeleGenre.getGenre(ligne);
            int comfirm = JOptionPane.showConfirmDialog(this, "Comfirmer suppression du genre : " + genre.getNomGenre() + " ? ", "Avertissement", JOptionPane.YES_NO_OPTION);
            if (comfirm == 0) {
                leModeleGenre.supprimerGenre(ligne);
            }

        } catch (Exception e) {
            System.out.println("Exception à la suppression : " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String reponse = JOptionPane.showInputDialog(this, "Entrer nom genre", "Ajouter genre", JOptionPane.OK_CANCEL_OPTION);
            if (reponse != null) {
                String newGenre = reponse;
                leModeleGenre.ajouterGenre(newGenre);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur à l'ajout du divorce : " + e.getMessage(), JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


